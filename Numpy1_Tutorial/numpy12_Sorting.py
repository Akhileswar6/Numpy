#Numpy arrays sorting
#sorting-> sorting means putting elements in an ordered sequence

import numpy as np
arr=np.array([3,78,45,2,1,67])
print(np.sort(arr))

arr2=np.array(['z','d','s','g','r','a','c'])
print(np.sort(arr2))

arr3=np.array([[23,98,12],[6,5,4]])
print(np.sort(arr3))

arr4=np.array([True,False,True])
print(np.sort(arr4))